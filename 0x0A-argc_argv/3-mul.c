#include <stdio.h>
#include <stdlib.h>

/**
 * main - function
 *
 * @argc: count
 * @argv: vector
 * Return: 0
 */
int main(int argc, char *argv[])
{
	if (argv[1])
	{
		printf("ERROR\n");

		return (1);
	}
	else
	{
		int i, j, k;
		/**
		 * i - mul
		 * j - argv 1
		 * k - argv 2
		 */

		j == argv[1];
		k == argv[2];

		i =(j * k);

		printf("%d\n", i);
	}
	(void) argc;
	return (0);
}
